    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Etudiant etudiant = getParams();
        int index;
        try {
            index = etudiantservice.save(etudiant);
            if (index > 0) {
                
                JOptionPane.showConfirmDialog(null, "Etudiant cr√©e avec succ√®s", "Informations", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showConfirmDialog(null, "Etudiant d√©j√† existant", "Erreur", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            Logger.getLogger(Stagiaire.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
             etudiantDao.save(etudiant);
             etudiantHelper.save(etudiant); 
        } catch (Exception ex) {
            Logger.getLogger(Stagiaire.class.getName()).log(Level.SEVERE, null, ex);
        }
                

    }//GEN-LAST:event_jButton1ActionPerformed

